# Numeric Types
age = 25  # int
height = 5.9  # float

# Text Type
message = "Hello, World!"  # str

# Boolean Type
is_python_fun = True  # bool

# Sequence Types
numbers_list = [1, 2, 3]  # list
coordinates = (4, 5, 6)  # tuple

# Set Type
unique_numbers = {1, 2, 3}  # set

# Mapping Type
person = {"name": "Alice", "age": 30}  # dict

# None Type
no_value = None  # NoneType

# Specialized Type
complex_number = 3 + 4j  # complex

# Printing the variables
print(age, type(age))
print(height, type(height))
print(message, type(message))
print(is_python_fun, type(is_python_fun))
print(numbers_list, type(numbers_list))
print(coordinates, type(coordinates))
print(unique_numbers, type(unique_numbers))
print(person, type(person))
print(no_value, type(no_value))
print(complex_number, type(complex_number))
