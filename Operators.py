# Arithmetic Operators
a = 10
b = 3
print("Arithmetic Operators:")
print(f"{a} + {b} =", a + b)
print(f"{a} - {b} =", a - b)
print(f"{a} * {b} =", a * b)
print(f"{a} / {b} =", a / b)
print(f"{a} % {b} =", a % b)
print(f"{a} ** {b} =", a ** b)
print(f"{a} // {b} =", a // b)

# Comparison Operators
x = 5
y = 10
print("\nComparison Operators:")
print(f"{x} == {y} :", x == y)
print(f"{x} != {y} :", x != y)
print(f"{x} < {y}  :", x < y)
print(f"{x} > {y}  :", x > y)
print(f"{x} <= {y} :", x <= y)
print(f"{x} >= {y} :", x >= y)

# Logical Operators
p = True
q = False
print("\nLogical Operators:")
print(f"{p} and {q} :", p and q)
print(f"{p} or {q}  :", p or q)
print(f"not {p}     :", not p)

# Assignment Operators
z = 5
z += 3
print("\nAssignment Operators:")
print(f"z += 3  => z =", z)

# Bitwise Operators
m = 5
n = 3
print("\nBitwise Operators:")
print(f"{m} & {n}   :", m & n)
print(f"{m} | {n}   :", m | n)
print(f"{m} ^ {n}   :", m ^ n)
print(f"~{m}        :", ~m)
print(f"{m} << 1   :", m << 1)
print(f"{m} >> 1   :", m >> 1)
